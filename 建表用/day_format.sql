CREATE TABLE `day_history` (
  `CODE` varchar(50) NOT NULL,
  `DATE` date NOT NULL,
  `VOLUME` decimal(20,3) DEFAULT NULL,
  `PRICE_OPEN` decimal(20,3) DEFAULT NULL,
  `PRICE_CLOSE` decimal(20,3) DEFAULT NULL,
  `PRICE_HIGH` decimal(20,3) DEFAULT NULL,
  `PRICE_LOW` decimal(20,3) DEFAULT NULL,
  `PRICE_CHANGE` decimal(20,3) DEFAULT NULL,
  `TURNOVER` varchar(45) DEFAULT NULL,
  `PRICE_AVERAGE_5` decimal(20,3) DEFAULT NULL,
  `PRICE_AVERAGE_10` decimal(20,3) DEFAULT NULL,
  `PRICE_AVERAGE_20` decimal(20,3) DEFAULT NULL,
  `VOLUME_AVERAGE_5` int DEFAULT NULL,
  `VOLUME_AVERAGE_10` int DEFAULT NULL,
  `VOLUME_AVERAGE_20` int DEFAULT NULL,
  `INSERT_DATETIME` datetime NOT NULL,
  `UPDATE_DATETIME` timestamp NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `UPDATE_NUMBER` int DEFAULT NULL,
  PRIMARY KEY (`CODE`,`DATE`)
) 
